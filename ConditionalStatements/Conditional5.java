

public class Conditional5 {
    public static void main(String[] args) {
        int marks = 90;
        if(marks>80)
        System.out.println("Grade A");
        else if(marks>60)
        System.out.println("Grade B");
        else if(marks>40)
        System.out.println("Pass");
        else
        System.out.println("Fail");
    }
}
